package src.basics_03;

public class Lab035 {
    public static void main(String[] args) {

        // Ternary operator.

        //int a =  condition ?  if true, print stmt1 :  If false ,print stmt2

        // MAX of two number

        int a = 35;
        int b = 45;
        int max = a < b ? b : a;
        int min = a < b ? a : b;
        System.out.println(max);
        System.out.println(min);

    }
}
