package src.basics_03;

public class Lab041 {
    public static void main(String[] args) {

        // Increment operator.
        int b = 20;
        System.out.println(++b + b++);
        System.out.println(b);

        // A - ++b -> Exp - 21 , b -> 21
        // B - b++ -> Exp - 21 , b -> 22
        // A+B -> 42
        /// b -> 22

    }
}
