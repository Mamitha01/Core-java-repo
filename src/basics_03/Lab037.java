public class Lab037 {
    public static void main(String[] args) {

        // Increment operator.
        // Pre and Post
        int a = 10;
//        a = a+1;
//        a +=1;
        ++a; // Pre Increment  = a = a+1; increment and print
        System.out.println(a);
        a++; // Post Increment - a = a+1; first print n then increment

        System.out.println(a);


    }
}
