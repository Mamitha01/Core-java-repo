package src.basics_02;

public class Lab004 {
    public static void main(String[] args) {

        // examples on Primitive datatypes

        byte b  = 10;
        short s = 123;
        int age = 34;

        // When we use the long variable we use the l or L in the end to identify as long
        long phone_number =  34L;
        long phone_number2 =  40L;

        // When we use the float, we have to use the f in the end to identify as float
        float f = 12.34F;
        float f2= 12.34f;
        double d = 12.34565678;

        //example of char datatype
        char c = 'A';
        char c1 = '&';
        char c2 = '$';
        char $ = '#';
        char _2 = '!';


        System.out.println(c);
        System.out.println(b);
        System.out.println(s);
        System.out.println(age);
        System.out.println(phone_number);
        System.out.println(f);
        System.out.println(d);
        System.out.println($);

    }
}
