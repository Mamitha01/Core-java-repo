package src.basics_02;

public class Lab009 {
    public static void main(String[] args) {
        char c = 'A';
        System.out.println(c);

//        char c1 = "$"; // String
        char c1 = '$';
        System.out.println(c1);


        char c2 = '\n';
        System.out.println(c2);

        // Unicode
        char c3 = '\u1F60';

        System.out.println(c3);

        // ASCII Table
        // 0-9 48-57
        // A-Z 65-90
        // a-z 97-122
//        // 0-9 48-57

        char ch10 = '\u8592';
        System.out.println(ch10);

        // Decimal System
        int age = 10;
        System.out.println(age);// Decimal

        // OCTal
        int b=0101;
        System.out.println(b);

        // Binary
        int b2 = 0b101;
        System.out.println(b2);

        // Hex
        int c45=0Xface;
        System.out.println(c45);


    }
}
