package src.basics_02;

public class Lab010 {
    public static void main(String[] args) {

        // Operators
        int age = 20;
        System.out.println(age);
        // = ----> Assignment operator - Store the value from right to left

        // Arithmetic Operator
        // -------> +, - , %, *
        double a = 20;
        double b = 54;
        double sum = a+b;
        System.out.printf("Sum of a and b is %f\n",sum);
        double sub = a-b;
        System.out.printf("Subtraction of a and b is %f\n",sub);
        double mul = a*b;
        System.out.printf("Multiplication of a and b is %f\n",mul);
        double div = a/b;
        System.out.printf("Quotient of a and b is %f\n",div);

    }
}
