package src.basics_05;

public class Lab078 {
    public static void main(String[] args) {


        // Print the Odd number in between the 1 to 50.
        // If Else
        for (int i = 0; i<=50; ++i) {
            if(!(i%2==0)){ // !(i%2==1)---> Odd OR (i%2==0)---> Even
                System.out.println(i+ " is Odd!");
            }
        }
        System.out.println(" --- End of Program!! --- ");





    }
}
